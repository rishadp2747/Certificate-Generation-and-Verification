    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
        $request->validate([
            'code' => 'required|unique:event',
            'name' => 'required',
        ]);

        $event = Event::create([
            'name' => $request->name,
            'code' => $request->code
        ]);

        $event->save();
        return back()->with('success','Successfully created event  ');

    }

